@implementation HPNItemsViewController

- (instancetype)init {
    self = [super initWithStyle:UITableViewStylePlain];
    if (self) {
        for (int i = 0; i < 5; i++) {
            [[HPNItemStore sharedStore] createItem];
        }
    }
    return self;
}

